def test_rmdocs(file, parsed_data):
    filepath = Path(__file__).parent.joinpath("input", file)
    with zipfile.ZipFile(filepath) as zf:
        files = list(parse_files_metadata(ZipPath(zf)))
    assert len(files) == 1
    assert files[0] == parsed_data
